# What is the default return value of to_s when invoked on an object?
# Where could you go to find out if you want to be sure?

# Answer:
# By default, the to_s method will return the name of the object's class and an encoding of the object id.
# You could find out how to_s works on objects by default in the Ruby docs:  http://ruby-doc.org/core-2.2.0/Object.html#method-i-to_s.